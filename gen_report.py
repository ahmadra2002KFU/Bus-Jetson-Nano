doc = Document()

def set_rtl_paragraph(p):
    pPr = p._p.get_or_add_pPr()
    pPr.set(qn('w:bidi'), '1')

def set_rtl_run(run, font_name='Traditional Arabic', size=14, bold=False, color=None):
    run.font.name = font_name
    run.font.size = Pt(size)
    run.font.bold = bold
    if color:
        run.font.color.rgb = color
    rPr = run._r.get_or_add_rPr()
    rPr.set(qn('w:rtl'), '1')
    rFonts = rPr.find(qn('w:rFonts'))
    if rFonts is None:
        rFonts = OxmlElement('w:rFonts')
        rPr.insert(0, rFonts)
    rFonts.set(qn('w:cs'), font_name)

def add_arabic_paragraph(text, size=14, bold=False, alignment=WD_ALIGN_PARAGRAPH.RIGHT, color=None, space_after=6, space_before=0):
    p = doc.add_paragraph()
    p.alignment = alignment
    p.paragraph_format.space_after = Pt(space_after)
    p.paragraph_format.space_before = Pt(space_before)
    set_rtl_paragraph(p)
    run = p.add_run(text)
    set_rtl_run(run, size=size, bold=bold, color=color)
    return p

def add_heading_arabic(text, size=18, color=RGBColor(0x1A, 0x3C, 0x6E)):
    p = add_arabic_paragraph(text, size=size, bold=True, color=color, space_before=12, space_after=6)
    pPr = p._p.get_or_add_pPr()
    pBdr = OxmlElement('w:pBdr')
    bottom = OxmlElement('w:bottom')
    bottom.set(qn('w:val'), 'single')
    bottom.set(qn('w:sz'), '8')
    bottom.set(qn('w:space'), '4')
    bottom.set(qn('w:color'), '1A3C6E')
    pBdr.append(bottom)
    pPr.append(pBdr)
    return p

def add_image_centered(path, width=Inches(4.5)):
    p = doc.add_paragraph()
    p.alignment = WD_ALIGN_PARAGRAPH.CENTER
    run = p.add_run()
    run.add_picture(path, width=width)
    return p

def add_image_caption(text):
    return add_arabic_paragraph(text, size=11, bold=False, alignment=WD_ALIGN_PARAGRAPH.CENTER, color=RGBColor(0x55, 0x55, 0x55))

